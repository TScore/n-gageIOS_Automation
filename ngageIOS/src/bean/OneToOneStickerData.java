package bean;

public class OneToOneStickerData 
{
	private String FriendName;
	private String noOfSticker;
	private String TypeOFSticker;
	public String getFriendName() {
		return FriendName;
	}
	public void setFriendName(String friendName) {
		FriendName = friendName;
	}
	public String getNoOfSticker() {
		return noOfSticker;
	}
	public void setNoOfSticker(String noOfSticker) {
		this.noOfSticker = noOfSticker;
	}
	public String getTypeOFSticker() {
		return TypeOFSticker;
	}
	public void setTypeOFSticker(String typeOFSticker) {
		TypeOFSticker = typeOFSticker;
	}
	
}
