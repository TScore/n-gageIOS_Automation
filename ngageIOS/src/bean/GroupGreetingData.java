package bean;

public class GroupGreetingData {
	private String FriendName;
	private String noOfGreeting;
	private String TypeOFGreeting;
	public String getFriendName() {
		return FriendName;
	}
	public void setFriendName(String friendName) {
		FriendName = friendName;
	}
	public String getNoOfGreeting() {
		return noOfGreeting;
	}
	public void setNoOfGreeting(String noOfGreeting) {
		this.noOfGreeting = noOfGreeting;
	}
	public String getTypeOFGreeting() {
		return TypeOFGreeting;
	}
	public void setTypeOFGreeting(String typeOFGreeting) {
		TypeOFGreeting = typeOFGreeting;
	}
	
}
