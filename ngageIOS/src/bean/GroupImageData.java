package bean;

public class GroupImageData {
	private String FriendName;
	private String noOfImage;
	private String TypeOFImage;
	public String getFriendName() {
		return FriendName;
	}
	public void setFriendName(String friendName) {
		FriendName = friendName;
	}
	public String getNoOfImage() {
		return noOfImage;
	}
	public void setNoOfImage(String noOfImage) {
		this.noOfImage = noOfImage;
	}
	public String getTypeOFImage() {
		return TypeOFImage;
	}
	public void setTypeOFImage(String typeOFImage) {
		TypeOFImage = typeOFImage;
	}
	
	
}
