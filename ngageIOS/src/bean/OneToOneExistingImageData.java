package bean;

public class OneToOneExistingImageData {
	private String FriendName;
	private String noOfExistingImage;
	private String TypeOfExistingImage;
	public String getFriendName() {
		return FriendName;
	}
	public void setFriendName(String friendName) {
		FriendName = friendName;
	}
	public String getNoOfExistingImage() {
		return noOfExistingImage;
	}
	public void setNoOfExistingImage(String noOfExistingImage) {
		this.noOfExistingImage = noOfExistingImage;
	}
	public String getTypeOfExistingImage() {
		return TypeOfExistingImage;
	}
	public void setTypeOfExistingImage(String typeOfExistingImage) {
		TypeOfExistingImage = typeOfExistingImage;
	}
	
}
